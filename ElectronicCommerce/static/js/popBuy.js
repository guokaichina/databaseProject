 function showPopBuy(){
        document.getElementsByClassName("popBuy")[0].style.display = "block";
        document.getElementById("hideBg2").style.display = "block";
    }
    // 关闭“购买”弹窗
 function closePopBuy() {
        document.getElementsByClassName("popBuy")[0].style.display = "none";
        document.getElementById("hideBg2").style.display = "none";
}